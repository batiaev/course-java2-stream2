package com.antipov.java2;

public class MyArrayDataException extends Exception {
    /**
     * Constructs a <code>MyArrayDataException</code> with no
     * detail message.
     */
    public MyArrayDataException() {super();}

    /**
     * Constructs a <code>MyArrayDataException</code> with the
     * specified detail message.
     *
     * @param   message   the detail message.
     */
    public MyArrayDataException(String message) {super(message);}
}
